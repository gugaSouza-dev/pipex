#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*s_dup;

	i = 0;
	s_dup = malloc(ft_strlen(s) + 1);
	if (!s_dup)
		return (0);
	while (s[i] != '\0')
	{
		s_dup[i] = s[i];
		i++;
	}
	s_dup[i] = '\0';
	return (s_dup);
}
